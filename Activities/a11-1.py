# Declare and assign x to 10
x = 10

# Decremental while loop to decrease the value of x by 1 in each iteration
while x >= 0:

    # If the value of x is more than 0, print the value of x
    if x > 0:
        print(x)

    # Otherwise print "Blast Off"
    else:
        print("Blast Off")

    # Decrement x by 1
    x -= 1
